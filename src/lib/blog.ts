/** Auto-seed the public blog on first server startup (same pattern as poster themes). */
export async function seedBlogIfEmpty(): Promise<void> {
  const count = await prisma.blogPost.count();
  if (count > 0) return;

  for (const post of SEED_BLOG_POSTS) {
    await prisma.blogPost.create({ data: { ...post, published: true } });
  }
}
